package com.bptn.weatherapp.response;

public class CoordResponse {

    private Double lon;
    private Double lat;


    // Getter Methods

    public Double getLon() {
        return lon;
    }

    public Double getLat() {
        return lat;
    }

    // Setter Methods

    public void setLon(Double lon) {
        this.lon = lon;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }
}
